CREATE OR REPLACE VIEW SCIM_USER_INFO AS
   SELECT A.USERNAME, B.ACTIVE,B.CREATEDATE,B.MODIFYDATE,B.EXPIREDATE,C.*
   FROM SCIM_USER A, SCIM_USER_META B, SCIM_USER_PROFILE C
   WHERE A.USERID = B.USERID AND B.USERID = C.USERID;
 
CREATE OR REPLACE VIEW SCIM_GROUP_INFO AS
   SELECT A.GROUPNAME, B.ACTIVE,B.CREATEDATE,B.MODIFYDATE,B.EXPIREDATE,C.*
   FROM SCIM_GROUP A, SCIM_GROUP_META B, SCIM_GROUP_PROFILE C
   WHERE A.GROUPID = B.GROUPID AND B.GROUPID = C.GROUPID;
   
   
CREATE OR REPLACE VIEW SCIM_USER_INFO AS
   SELECT A.ACTIVE, B.*
   FROM SCIM_USER A, 
        (SELECT
            USERID,
            MAX(DECODE(pkey, 'employeeNumber', pvalue)) AS employeeNumber,
            MAX(DECODE(pkey, 'username', pvalue)) AS username,
            MAX(DECODE(pkey, 'OrganizationPath', pvalue)) AS OrganizationPath
            FROM
                SCIM_USER_PROFILE
            GROUP BY USERID
            ORDER BY USERID) B
    WHERE A.USERID = B.USERID;

